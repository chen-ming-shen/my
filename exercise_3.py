#利用map()函数，把用户输入的不规范的英文名字，变为首字母大写，其他小写的规范名字。输入：['adam', 'LISA', 'barT']，输出：['Adam', 'Lisa', 'Bart']：
def normalize(name):
    #lower全部改为小写
    #upper全部改为大写
    #capitalize首字母大写其他小写
    name=name.capitalize()
    return name
# 测试:
